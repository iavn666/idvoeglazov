public class Factorial {
    /**
     * calc
     * @param n входящий параметр.
     * @return fact возвращаемый.
     */
    public int calc(int n) {
        int fact = 1;
        for (int a = 1; a <= n; a++) {
          if (a > 0);
            fact *= a;
            }
          return fact;

    }
}
